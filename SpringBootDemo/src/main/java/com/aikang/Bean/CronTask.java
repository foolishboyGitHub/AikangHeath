package com.aikang.Bean;

public class CronTask {
	
	private long id;
	
	private String company;
	
	private String tabe;
	
	private long tabeid;
	
	private String last;
	
	private Integer cycle;
	
	public long getId() {
		return id;
	}

	public void setId(long id) {
		this.id = id;
	}

	public String getCompany() {
		return company;
	}

	public void setCompany(String company) {
		this.company = company;
	}

	public String getTabe() {
		return tabe;
	}

	public void setTabe(String tabe) {
		this.tabe = tabe;
	}

	public long getTabeid() {
		return tabeid;
	}

	public void setTabeid(long tabeid) {
		this.tabeid = tabeid;
	}

	public String getLast() {
		return last;
	}

	public void setLast(String last) {
		this.last = last;
	}

	public Integer getCycle() {
		return cycle;
	}

	public void setCycle(Integer cycle) {
		this.cycle = cycle;
	}
	
}
